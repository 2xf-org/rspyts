fn all_assignment(names: &[String]) -> String {
    let mut seen = std::collections::BTreeSet::new();
    let names = names
        .iter()
        .filter(|name| seen.insert(name.as_str()))
        .collect::<Vec<_>>();
    if names.is_empty() {
        return "__all__: __rspyts_builtins__.list[__rspyts_builtins__.str] = []\n\n".into();
    }
    format!(
        "__all__ = [\n{}]\n\n",
        names
            .iter()
            .map(|name| format!("    {},\n", python_string_literal(name)))
            .collect::<String>()
    )
}

fn python_ref(reference: &TypeRef, names: &TypeNames) -> String {
    match reference {
        TypeRef::Unit => "None".into(),
        TypeRef::Bool => "__rspyts_builtins__.bool".into(),
        TypeRef::Int { signed, bits } => {
            let (minimum, maximum) = intrinsic_integer_bounds(*signed, *bits);
            format!(
                "__rspyts_Annotated__[__rspyts_builtins__.int, __rspyts_Field__(ge={minimum}, le={maximum})]"
            )
        }
        TypeRef::Float { bits: 32 } => concat!(
            "__rspyts_Annotated__[__rspyts_builtins__.float, ",
            "__rspyts_Field__(ge=-3.4028234663852886e38, ",
            "le=3.4028234663852886e38, allow_inf_nan=False)]"
        )
        .into(),
        TypeRef::Float { bits: 64 } => concat!(
            "__rspyts_Annotated__[__rspyts_builtins__.float, ",
            "__rspyts_Field__(ge=-1.7976931348623157e308, ",
            "le=1.7976931348623157e308, allow_inf_nan=False)]"
        )
        .into(),
        TypeRef::Float { bits } => unreachable!("validated float width {bits}"),
        TypeRef::String => "__rspyts_builtins__.str".into(),
        TypeRef::DateTime => "__rspyts_AwareDatetime__".into(),
        TypeRef::Json => "JsonValue".into(),
        TypeRef::Option { item } => format!("{} | None", python_ref(item, names)),
        TypeRef::List { item } => {
            format!("__rspyts_builtins__.list[{}]", python_ref(item, names))
        }
        TypeRef::Map { value } => format!(
            "__rspyts_builtins__.dict[__rspyts_builtins__.str, {}]",
            python_ref(value, names)
        ),
        TypeRef::Tuple { items } => format!(
            "__rspyts_builtins__.tuple[{}]",
            items
                .iter()
                .map(|item| python_ref(item, names))
                .collect::<Vec<_>>()
                .join(", ")
        ),
        TypeRef::Named { identity } => names
            .get(identity)
            .cloned()
            .unwrap_or_else(|| identity.to_string()),
        TypeRef::Bytes => "__rspyts_builtins__.bytes".into(),
        TypeRef::FixedBytes { length } => {
            format!(
                "__rspyts_Annotated__[__rspyts_builtins__.bytes, __rspyts_Field__(min_length={length}, max_length={length})]"
            )
        }
        TypeRef::Buffer { element } => {
            format!(
                "__rspyts_NDArray__[__rspyts_np__.{}]",
                numpy_scalar(*element)
            )
        }
    }
}

fn python_field_ref(
    reference: &TypeRef,
    names: &TypeNames,
    contract: &ResolvedContract,
    minimum: Option<i64>,
) -> String {
    let Some(minimum) = minimum else {
        return python_ref(reference, names);
    };
    match reference {
        TypeRef::Int { signed, bits } => {
            let (intrinsic_minimum, maximum) = intrinsic_integer_bounds(*signed, *bits);
            let minimum = i128::from(minimum).max(intrinsic_minimum);
            format!(
                "__rspyts_Annotated__[__rspyts_builtins__.int, __rspyts_Field__(ge={minimum}, le={maximum})]"
            )
        }
        TypeRef::Float { bits: 32 } => {
            let minimum = (minimum as f64).max(-3.4028234663852886e38);
            format!(
                "__rspyts_Annotated__[__rspyts_builtins__.float, __rspyts_Field__(ge={minimum}, le=3.4028234663852886e38, allow_inf_nan=False)]"
            )
        }
        TypeRef::Float { bits: 64 } => {
            let minimum = (minimum as f64).max(-1.7976931348623157e308);
            format!(
                "__rspyts_Annotated__[__rspyts_builtins__.float, __rspyts_Field__(ge={minimum}, le=1.7976931348623157e308, allow_inf_nan=False)]"
            )
        }
        TypeRef::Option { item } => format!(
            "{} | None",
            python_field_ref(item, names, contract, Some(minimum))
        ),
        TypeRef::Named { identity } => match type_definition(contract, identity) {
            Some(TypeDef {
                shape: TypeShape::Alias { target },
                ..
            }) => python_field_ref(target, names, contract, Some(minimum)),
            _ => python_ref(reference, names),
        },
        _ => python_ref(reference, names),
    }
}

fn intrinsic_integer_bounds(signed: bool, bits: u16) -> (i128, i128) {
    match (signed, bits) {
        (true, 8 | 16 | 32 | 64) => {
            let magnitude = 1_i128 << (bits - 1);
            (-magnitude, magnitude - 1)
        }
        (false, 8 | 16 | 32 | 64) => (0, (1_i128 << bits) - 1),
        _ => unreachable!("validated integer width {bits}"),
    }
}

fn python_to_native(reference: &TypeRef, expression: &str, contract: &ResolvedContract) -> String {
    match reference {
        TypeRef::Named { identity } => {
            match type_definition(contract, identity).map(|item| &item.shape) {
                Some(TypeShape::Struct { fields }) => format!(
                    "{{{}}}",
                    fields
                        .iter()
                        .map(|field| format!(
                            "{}: {}",
                            python_string_literal(&field.wire_name),
                            python_to_native(
                                &field.ty,
                                &format!("{expression}.{}", field.rust_name),
                                contract
                            )
                        ))
                        .collect::<Vec<_>>()
                        .join(", ")
                ),
                Some(TypeShape::TaggedEnum { tag, variants }) => {
                    python_tagged_enum_to_native(tag, variants, expression, contract)
                }
                Some(TypeShape::StringEnum { .. }) => format!("{expression}.value"),
                Some(TypeShape::Alias { target }) => python_to_native(target, expression, contract),
                None => expression.to_owned(),
            }
        }
        TypeRef::Option { item } => format!(
            "None if {expression} is None else {}",
            python_to_native(item, expression, contract)
        ),
        TypeRef::List { item } => format!(
            "[{} for __rspyts_item in {expression}]",
            python_to_native(item, "__rspyts_item", contract)
        ),
        TypeRef::Map { value } => format!(
            "{{__rspyts_key: {} for __rspyts_key, __rspyts_item in {expression}.items()}}",
            python_to_native(value, "__rspyts_item", contract)
        ),
        TypeRef::Tuple { items } => {
            let converted = items
                .iter()
                .enumerate()
                .map(|(index, item)| {
                    python_to_native(item, &format!("({expression})[{index}]"), contract)
                })
                .collect::<Vec<_>>()
                .join(", ");
            let trailing_comma = if items.len() == 1 { "," } else { "" };
            let message = python_string_literal(&format!(
                "expected tuple of length {}, received a different length",
                items.len()
            ));
            format!(
                "(({converted}{trailing_comma}) if ({expression}).__len__() == {} else (__rspyts_item for __rspyts_item in ()).throw(__rspyts_builtins__.ValueError({message})))",
                items.len()
            )
        }
        TypeRef::Buffer { element } => {
            format!(
                "__rspyts_encode_buffer__({expression}, {})",
                python_string_literal(buffer_name(*element))
            )
        }
        TypeRef::DateTime => format!("{expression}.isoformat()"),
        _ => expression.to_owned(),
    }
}

fn python_tagged_enum_to_native(
    tag: &str,
    variants: &[rspyts::ir::EnumVariantDef],
    expression: &str,
    contract: &ResolvedContract,
) -> String {
    let tag_field = python_tag_field(tag, variants);
    let variant_value = |variant: &rspyts::ir::EnumVariantDef| {
        let fields = std::iter::once(format!(
            "{}: {}",
            python_string_literal(tag),
            python_string_literal(&variant.wire_name)
        ))
        .chain(variant.fields.iter().map(|field| {
            format!(
                "{}: {}",
                python_string_literal(&field.wire_name),
                python_to_native(
                    &field.ty,
                    &format!("({expression}).{}", field.rust_name),
                    contract,
                )
            )
        }))
        .collect::<Vec<_>>()
        .join(", ");
        format!("{{{fields}}}")
    };
    let invalid_message = python_string_literal(&format!(
        "unknown discriminator for tagged enum tag `{tag}`"
    ));
    let mut rendered = format!(
        "(__rspyts_item for __rspyts_item in ()).throw(__rspyts_builtins__.ValueError({invalid_message}))"
    );
    for variant in variants.iter().rev() {
        let wire_name = python_string_literal(&variant.wire_name);
        rendered = format!(
            "({} if ({expression}).{tag_field} == {wire_name} else {rendered})",
            variant_value(variant),
        );
    }
    rendered
}

fn python_from_native(
    reference: &TypeRef,
    expression: &str,
    contract: &ResolvedContract,
) -> String {
    match reference {
        TypeRef::Named { identity } => match type_definition(contract, identity) {
            Some(TypeDef {
                name,
                shape: TypeShape::Struct { fields },
                ..
            }) => format!(
                "__rspyts_models__.{name}.model_validate({})",
                python_object_from_native(fields, expression, contract)
            ),
            Some(TypeDef {
                name,
                shape: TypeShape::TaggedEnum { tag, variants },
                ..
            }) => python_tagged_enum_from_native(name, tag, variants, expression, contract),
            Some(TypeDef {
                name,
                shape: TypeShape::StringEnum { .. },
                ..
            }) => format!("__rspyts_models__.{name}({expression})"),
            Some(TypeDef {
                shape: TypeShape::Alias { target },
                ..
            }) => python_from_native(target, expression, contract),
            _ => expression.to_owned(),
        },
        TypeRef::Option { item } => format!(
            "None if {expression} is None else {}",
            python_from_native(item, expression, contract)
        ),
        TypeRef::List { item } => format!(
            "[{} for __rspyts_item in {expression}]",
            python_from_native(item, "__rspyts_item", contract)
        ),
        TypeRef::Map { value } => format!(
            "{{__rspyts_key: {} for __rspyts_key, __rspyts_item in {expression}.items()}}",
            python_from_native(value, "__rspyts_item", contract)
        ),
        TypeRef::Tuple { items } => format!(
            "({})",
            items
                .iter()
                .enumerate()
                .map(|(index, item)| {
                    python_from_native(item, &format!("{expression}[{index}]"), contract)
                })
                .collect::<Vec<_>>()
                .join(", ")
        ),
        TypeRef::Buffer { element } => {
            format!(
                "__rspyts_decode_buffer__({expression}, {})",
                python_string_literal(buffer_name(*element))
            )
        }
        TypeRef::DateTime => {
            format!(
                "__rspyts_TypeAdapter__(__rspyts_AwareDatetime__).validate_python({expression})"
            )
        }
        _ => expression.to_owned(),
    }
}

fn python_tagged_enum_from_native(
    name: &str,
    tag: &str,
    variants: &[rspyts::ir::EnumVariantDef],
    expression: &str,
    contract: &ResolvedContract,
) -> String {
    let tag_key = python_string_literal(tag);
    let variant_value = |variant: &rspyts::ir::EnumVariantDef| {
        python_object_from_native(&variant.fields, expression, contract)
    };
    let (last, remaining) = variants
        .split_last()
        .expect("tagged enum variants are validated before Python emission");
    let mut rendered = variant_value(last);
    for variant in remaining.iter().rev() {
        let wire_name = python_string_literal(&variant.wire_name);
        rendered = format!(
            "({} if {expression}[{tag_key}] == {wire_name} else {rendered})",
            variant_value(variant),
        );
    }
    format!("__rspyts_TypeAdapter__(__rspyts_models__.{name}).validate_python({rendered})")
}

fn python_object_from_native(
    fields: &[FieldDef],
    expression: &str,
    contract: &ResolvedContract,
) -> String {
    let overrides = fields
        .iter()
        .map(|field| {
            let key = python_string_literal(&field.wire_name);
            format!(
                "**({{{key}: {}}} if {key} in {expression} else {{}})",
                python_from_native(&field.ty, &format!("{expression}[{key}]"), contract)
            )
        })
        .collect::<Vec<_>>()
        .join(", ");
    if overrides.is_empty() {
        format!("{{**{expression}}}")
    } else {
        format!("{{**{expression}, {overrides}}}")
    }
}

