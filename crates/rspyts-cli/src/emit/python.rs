fn python_value(value: &Value, ty: &TypeRef, contract: &ResolvedContract) -> String {
    match (value, ty) {
        (Value::Null, _) => "None".into(),
        (Value::Bool(value), _) => if *value { "True" } else { "False" }.into(),
        (Value::Number(value), _) => value.to_string(),
        (Value::String(value), TypeRef::Named { identity }) => {
            match type_definition(contract, identity) {
                Some(TypeDef {
                    name,
                    shape: TypeShape::StringEnum { .. },
                    ..
                }) => format!("__rspyts_models__.{name}({})", python_string_literal(value)),
                Some(TypeDef {
                    shape: TypeShape::Alias { target },
                    ..
                }) => python_value(&Value::String(value.clone()), target, contract),
                _ => python_string_literal(value),
            }
        }
        (Value::String(value), TypeRef::DateTime) => {
            format!(
                "__rspyts_TypeAdapter__(__rspyts_AwareDatetime__).validate_python({})",
                python_string_literal(value)
            )
        }
        (Value::String(value), _) => python_string_literal(value),
        (Value::Array(items), TypeRef::List { item }) => format!(
            "[{}]",
            items
                .iter()
                .map(|value| python_value(value, item, contract))
                .collect::<Vec<_>>()
                .join(", ")
        ),
        (Value::Array(items), TypeRef::Tuple { items: types }) => format!(
            "({}{})",
            items
                .iter()
                .zip(types)
                .map(|(value, ty)| python_value(value, ty, contract))
                .collect::<Vec<_>>()
                .join(", "),
            if items.len() == 1 { "," } else { "" }
        ),
        (Value::Array(items), TypeRef::Bytes | TypeRef::FixedBytes { .. }) => format!(
            "__rspyts_builtins__.bytes([{}])",
            items
                .iter()
                .map(Value::to_string)
                .collect::<Vec<_>>()
                .join(", ")
        ),
        (Value::Array(items), TypeRef::Buffer { element }) => format!(
            "__rspyts_np__.array([{}], dtype=__rspyts_np__.dtype({}))",
            items
                .iter()
                .map(Value::to_string)
                .collect::<Vec<_>>()
                .join(", "),
            python_string_literal(numpy_dtype(*element)),
        ),
        (Value::Object(items), TypeRef::Map { value }) => format!(
            "{{{}}}",
            items
                .iter()
                .map(|(key, item)| format!(
                    "{}: {}",
                    python_string_literal(key),
                    python_value(item, value, contract)
                ))
                .collect::<Vec<_>>()
                .join(", ")
        ),
        (value, TypeRef::Option { item }) => python_value(value, item, contract),
        (value, TypeRef::Named { identity }) => match type_definition(contract, identity) {
            Some(TypeDef {
                name,
                shape: TypeShape::Struct { fields },
                ..
            }) => format!(
                "__rspyts_models__.{name}.model_validate({})",
                python_object_value(value, fields, contract)
            ),
            Some(TypeDef {
                name,
                shape: TypeShape::TaggedEnum { tag, variants },
                ..
            }) => {
                let fields = value
                    .as_object()
                    .and_then(|items| items.get(tag))
                    .and_then(Value::as_str)
                    .and_then(|wire_name| {
                        variants
                            .iter()
                            .find(|variant| variant.wire_name == wire_name)
                    })
                    .map(|variant| variant.fields.as_slice())
                    .unwrap_or_default();
                format!(
                    "__rspyts_TypeAdapter__(__rspyts_models__.{name}).validate_python({})",
                    python_object_value(value, fields, contract)
                )
            }
            Some(TypeDef {
                shape: TypeShape::Alias { target },
                ..
            }) => python_value(value, target, contract),
            _ => serde_json::to_string(value).expect("JSON value is serializable"),
        },
        (Value::Array(items), _) => format!(
            "[{}]",
            items.iter().map(python_json).collect::<Vec<_>>().join(", ")
        ),
        (Value::Object(items), _) => format!(
            "{{{}}}",
            items
                .iter()
                .map(|(key, value)| format!(
                    "{}: {}",
                    python_string_literal(key),
                    python_json(value)
                ))
                .collect::<Vec<_>>()
                .join(", ")
        ),
    }
}

fn python_object_value(value: &Value, fields: &[FieldDef], contract: &ResolvedContract) -> String {
    let Value::Object(items) = value else {
        return python_json(value);
    };
    format!(
        "{{{}}}",
        items
            .iter()
            .map(|(key, value)| {
                let rendered = fields
                    .iter()
                    .find(|field| field.wire_name == *key)
                    .map_or_else(
                        || python_json(value),
                        |field| python_value(value, &field.ty, contract),
                    );
                format!("{}: {rendered}", python_string_literal(key))
            })
            .collect::<Vec<_>>()
            .join(", ")
    )
}

fn python_json(value: &Value) -> String {
    match value {
        Value::Null => "None".into(),
        Value::Bool(value) => if *value { "True" } else { "False" }.into(),
        Value::Number(value) => value.to_string(),
        Value::String(value) => python_string_literal(value),
        Value::Array(values) => format!(
            "[{}]",
            values
                .iter()
                .map(python_json)
                .collect::<Vec<_>>()
                .join(", ")
        ),
        Value::Object(values) => format!(
            "{{{}}}",
            values
                .iter()
                .map(|(key, value)| format!(
                    "{}: {}",
                    python_string_literal(key),
                    python_json(value)
                ))
                .collect::<Vec<_>>()
                .join(", ")
        ),
    }
}

