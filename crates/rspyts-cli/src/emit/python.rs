fn functions(contract: &ResolvedContract, names: &TypeNames) -> String {
    let manifest = &contract.manifest;
    let mut output = format!(
        "# Generated by rspyts 0.4. Do not edit.\n\
from __future__ import annotations\n\n\
import builtins as __rspyts_builtins__\n\n\
from . import {} as __rspyts_native__\n\
from . import errors as __rspyts_errors__\n\
from . import models as __rspyts_models__\n\
from .errors import *  # noqa: F403\n\
from .models import *  # noqa: F403\n\n",
        manifest.module_name
    );
    if functions_use_adapter(contract) {
        output.push_str(
            "from pydantic import AwareDatetime as __rspyts_AwareDatetime__\n\
from pydantic import TypeAdapter as __rspyts_TypeAdapter__\n\n",
        );
    }
    if functions_use_inline_annotations(contract) {
        output.push_str(
            "from typing import Annotated as __rspyts_Annotated__\n\
from pydantic import Field as __rspyts_Field__\n\n",
        );
    }
    if functions_use_buffer(contract) {
        output.push_str(
            "from .codecs import __rspyts_decode_buffer__, __rspyts_encode_buffer__\n\
import numpy as __rspyts_np__\n\
from numpy.typing import NDArray as __rspyts_NDArray__\n\n",
        );
    }
    output.push_str(&all_assignment(
        &manifest
            .functions
            .iter()
            .filter(|function| matches!(function.target, Target::Both | Target::Python))
            .map(|function| function.rust_name.clone())
            .collect::<Vec<_>>(),
    ));
    for function in manifest
        .functions
        .iter()
        .filter(|function| matches!(function.target, Target::Both | Target::Python))
    {
        let params = function
            .params
            .iter()
            .map(|param| format!("{}: {}", param.rust_name, python_ref(&param.ty, names)))
            .collect::<Vec<_>>();
        let args = function
            .params
            .iter()
            .map(|param| param.rust_name.as_str())
            .collect::<Vec<_>>();
        let call = format!(
            "__rspyts_native__.{}({})",
            function.host_name,
            function
                .params
                .iter()
                .zip(args)
                .map(|(param, argument)| python_to_native(&param.ty, argument, contract))
                .collect::<Vec<_>>()
                .join(", ")
        );
        let body = match error_name(function.error.as_ref(), contract) {
            Some(error) => format!(
                "    try:\n        __rspyts_result = {call}\n    except __rspyts_builtins__.RuntimeError as __rspyts_error:\n        raise __rspyts_errors__.__rspyts_translate_error__(\n            __rspyts_error, __rspyts_errors__.{error}\n        ) from None\n    except (\n        __rspyts_builtins__.TypeError,\n        __rspyts_builtins__.ValueError,\n        __rspyts_builtins__.OverflowError,\n    ) as __rspyts_error:\n        raise __rspyts_errors__.__rspyts_translate_boundary_error__(\n            __rspyts_error, __rspyts_errors__.{error}\n        ) from None\n"
            ),
            None => format!("    __rspyts_result = {call}\n"),
        };
        output.push_str(&format!(
            "def {}({}) -> {}:\n{}{}    return {}\n\n",
            function.rust_name,
            params.join(", "),
            python_ref(&function.returns, names),
            python_doc(function.docs.as_deref(), "    "),
            body,
            python_from_native(&function.returns, "__rspyts_result", contract)
        ));
    }
    output
}

